     private void  popularTabela()
    {
        classes.AddCertificado p = new AddCertificado("","","","","","");
       ArrayList<classes.AddCertificado> pList = p.certList();
       
       String [] colNames ={"Nome_Aeroporto","Entidade_Responsavel","Data_Certificacao","Data_Supervisao","Armario","Prateleira"};
       Object [][] rows=new Object[pList.size()][colNames.length];
       for(int i=0;i<pList.size();i++)
       {
           rows[i][0]=pList.get(i).getNome_aeroporto();
           rows[i][1]=pList.get(i).getEntidade_responsavel();
           rows[i][2]=pList.get(i).getData_certificacao();
           rows[i][3]=pList.get(i).getData_supervisao();
           rows[i][4]=pList.get(i).getArmario();
           rows[i][5]=pList.get(i).getPrateleira();
           
       }
       DefaultTableModel model =  new DefaultTableModel(rows,colNames);
       jTable1.setModel(model);
       
    }
