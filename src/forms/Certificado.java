    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String nome_aeroporto=jTextField1.getText();
         String entidade_responsavel=jTextField2.getText();
         final DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
         String data_certificacao=df.format(jDateChooser1.getDate());;
         String data_supervisao=df.format(jDateChooser2.getDate());;
         String armario=jTextField5.getText();
         String prateleira=jTextField6.getText();
          if(nome_aeroporto.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Fields Empty","",2);
        }
        else
        {
            classes.AddCertificado addp = new AddCertificado("","","","","","");
            addp.addCERT(nome_aeroporto,entidade_responsavel,data_certificacao,data_supervisao,armario,prateleira);
        }
        popularTabela();

    }//GEN-LAST:event_jButton1ActionPerformed

