    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String nome = jTextField1.getText();
        String pais = jTextField2.getText();
        String loc = jTextField3.getText();
         final DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String data_Assinatura =df.format(jDateChooser1.getDate());
        String data_Registro =df.format(jDateChooser2.getDate());
        String armario = jTextField6.getText();
        String prateleira = jTextField7.getText();
        
        
        if(nome.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Fields Empty","",2);
        }
        else
        {
            classes.Acordoss addp = new Acordoss("","","","","","","");
            addp.addAcordos(nome,pais,data_Assinatura,loc,data_Registro,prateleira,armario);
        }
        popularTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

