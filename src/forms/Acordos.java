    private void  popularTabela()
    {
        classes.Acordoss p = new Acordoss("","","","","","","");
       ArrayList<classes.Acordoss> pList = p.acordotList();
       
       String [] colNames ={"Nome_Acordo","Pais","Data_Assinatura","Local_Assinatura","Data_Registro","Prateleira","Armario"};
       Object [][] rows=new Object[pList.size()][colNames.length];
       for(int i=0;i<pList.size();i++)
       {
           rows[i][0]=pList.get(i).getNome_acordo();
           rows[i][1]=pList.get(i).getPais();
           rows[i][2]=pList.get(i).getData_Assinatura();
           rows[i][3]=pList.get(i).getLocal_assinatura();
           rows[i][4]=pList.get(i).getData_registro();
           rows[i][5]=pList.get(i).getPrateleira();
           rows[i][6]=pList.get(i).getArmario();
       }
       DefaultTableModel model =  new DefaultTableModel(rows,colNames);
       jTable1.setModel(model);
       
    }
