    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome_ato =jTextField1.getText();
        String pais= jTextField2.getText();
        String capital= jTextField3.getText();
        String armario =jTextField6.getText();
        String prateleira =jTextField7.getText();
        final DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        //final Date date = rs.getDate();
        String data_criacao =df.format(jDateChooser1.getDate());
        String data_homo =df.format(jDateChooser2.getDate());
        if(nome_ato.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Fields Empty","",2);
        }
        else
        {
            classes.AddATO addp = new AddATO("","","","","","","");
            addp.addATO(nome_ato,  pais,  capital,  data_criacao,  data_homo, armario,  prateleira);
        }
        popularTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

