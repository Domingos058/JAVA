    private void  popularTabela()
    {
        classes.AddATO p = new AddATO("","","","","","","");
       ArrayList<classes.AddATO> pList = p.atotList();
       
       String [] colNames ={"Nome_Ato","Pais","Capital","Data_Criacao","Data_Homo","Armario","Prateleira"};
       Object [][] rows=new Object[pList.size()][colNames.length];
       for(int i=0;i<pList.size();i++)
       {
           rows[i][0]=pList.get(i).getNome_ato();
           rows[i][1]=pList.get(i).getPais();
           rows[i][2]=pList.get(i).getCapital();
           rows[i][3]=pList.get(i).getData_criacao();
           rows[i][4]=pList.get(i).getData_homo();
           rows[i][5]=pList.get(i).getArmario();
           rows[i][6]=pList.get(i).getPrateleira();
           
       }
       DefaultTableModel model =  new DefaultTableModel(rows,colNames);
       jTable1.setModel(model);
       
    }
    
    
